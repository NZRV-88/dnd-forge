// Функция для получения мастерства по ключу
export const getWeaponMasteryByKey = (key: string): WeaponMastery | undefined => {
    return WEAPON_MASTERY.find(mastery => mastery.key === key);
};

// Функция для получения полного описания мастерства
export const getWeaponMasteryDescription = (key: string): string => {
    const mastery = getWeaponMasteryByKey(key);
    if (!mastery) return key;
    
    return `${mastery.name} (${mastery.nameEn}). ${mastery.desc}`;
};
